package io.leafage.hypervisor.bo;

import java.net.InetAddress;

/**
 * bo class for audit log
 *
 * @author wq li
 */
public abstract class AuditLogBO {

    private String operator;

    private String operation;

    private String resource;

    private String oldValue;

    private String newValue;

    private InetAddress ip;

    private String location;

    private Integer statusCode;

    private Long operatedTimes;


    public String getOperator() {
        return operator;
    }

    public void setOperator(String operator) {
        this.operator = operator;
    }

    public String getOperation() {
        return operation;
    }

    public void setOperation(String operation) {
        this.operation = operation;
    }

    public String getResource() {
        return resource;
    }

    public void setResource(String resource) {
        this.resource = resource;
    }

    public String getOldValue() {
        return oldValue;
    }

    public void setOldValue(String oldValue) {
        this.oldValue = oldValue;
    }

    public String getNewValue() {
        return newValue;
    }

    public void setNewValue(String newValue) {
        this.newValue = newValue;
    }

    public InetAddress getIp() {
        return ip;
    }

    public void setIp(InetAddress ip) {
        this.ip = ip;
    }

    public String getLocation() {
        return location;
    }

    public void setLocation(String location) {
        this.location = location;
    }

    public Integer getStatusCode() {
        return statusCode;
    }

    public void setStatusCode(Integer statusCode) {
        this.statusCode = statusCode;
    }

    public Long getOperatedTimes() {
        return operatedTimes;
    }

    public void setOperatedTimes(Long operatedTimes) {
        this.operatedTimes = operatedTimes;
    }
}
